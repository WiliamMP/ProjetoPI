    @Override
    public void cadDados() {
       try {
            FileOutputStream file = new FileOutputStream("Infor.txt");
            ObjectOutputStream Of = new ObjectOutputStream(file);
            for (int i = 0; i < dn.size(); i++) {
                Of.writeObject(dn.get(i));
            }
            JOptionPane.showMessageDialog(null, "Envio de dados completo!");
            Of.close();
        } catch (IOException e) {
            
            
        } 
    }

    @Override
    public void verDados() {
        try{
            FileInputStream file = new FileInputStream("Infor.txt");
            ObjectInputStream Inf = new ObjectInputStream(file);
            boolean endOfFile = false;
            
            while(!endOfFile){
                try {
                dn.add((Dono)Inf.readObject());
              
            } catch (EOFException f) {
                endOfFile = true;
            } catch(Exception e) {
                
            }
            
}
        }catch(IOException e){
    }
    }

